// Renders a Certificate of Origin PDF from document.content using jsPDF + autoTable

export function renderCOToPdf(document) {
  const jsPDF = window.jspdf?.jsPDF;
  if (!jsPDF) { alert('PDF 라이브러리가 로드되지 않았습니다.'); return; }

  const c = document.content || {};
  const doc = new jsPDF('p', 'mm', 'a4');
  const pageWidth = doc.internal.pageSize.getWidth();
  const margin = 15;
  let y = margin;

  // Header
  doc.setFontSize(20);
  doc.setFont(undefined, 'bold');
  doc.setTextColor(5, 150, 105);
  doc.text('CERTIFICATE OF ORIGIN', pageWidth / 2, y, { align: 'center' });
  y += 10;

  doc.setTextColor(0);
  doc.setFontSize(10);
  doc.setFont(undefined, 'normal');
  const docNo = document.doc_number || 'N/A';
  const dateStr = c.generated_at ? new Date(c.generated_at).toLocaleDateString('en-US') : new Date().toLocaleDateString('en-US');
  doc.text(`Certificate No: ${docNo}`, margin, y);
  doc.text(`Date: ${dateStr}`, pageWidth - margin, y, { align: 'right' });
  y += 5;
  doc.setDrawColor(5, 150, 105);
  doc.setLineWidth(0.5);
  doc.line(margin, y, pageWidth - margin, y);
  y += 8;

  // Exporter / Consignee
  doc.setFontSize(9);
  const halfWidth = (pageWidth - margin * 2) / 2;
  doc.setFont(undefined, 'bold');
  doc.text('EXPORTER', margin, y);
  doc.text('CONSIGNEE', margin + halfWidth + 5, y);
  y += 5;
  doc.setFont(undefined, 'normal');
  const exporter = c.exporter || c.seller || {};
  const consignee = c.consignee || c.buyer || {};
  const exporterLines = [
    exporter.company || exporter.name || 'N/A',
    exporter.address || '',
    exporter.email || '',
  ].filter(Boolean);
  const consigneeLines = [
    consignee.company || consignee.name || 'N/A',
    consignee.address || '',
    consignee.email || '',
  ].filter(Boolean);
  const maxLines = Math.max(exporterLines.length, consigneeLines.length);
  for (let i = 0; i < maxLines; i++) {
    if (exporterLines[i]) doc.text(exporterLines[i], margin, y);
    if (consigneeLines[i]) doc.text(consigneeLines[i], margin + halfWidth + 5, y);
    y += 4.5;
  }
  y += 4;

  // Country of Origin
  doc.setFont(undefined, 'bold');
  doc.setFontSize(11);
  doc.text('Country of Origin: REPUBLIC OF KOREA', margin, y);
  y += 8;

  // HS Code & Transport
  doc.setFontSize(9);
  doc.setFont(undefined, 'normal');
  const infoLines = [];
  if (c.hs_code) infoLines.push(`HS Code: ${c.hs_code}`);
  if (c.transport_mode) infoLines.push(`Transport: ${c.transport_mode}`);
  if (c.destination_country) infoLines.push(`Destination: ${c.destination_country}`);
  if (infoLines.length) {
    doc.text(infoLines.join('  |  '), margin, y);
    y += 7;
  }

  // Goods table
  const items = Array.isArray(c.items) ? c.items : [];
  if (items.length > 0) {
    const tableData = items.map((item, idx) => [
      idx + 1,
      item.hs_code || c.hs_code || '-',
      item.product_name || item.description || 'N/A',
      fmt(item.qty),
      item.unit || 'PCS',
    ]);

    doc.autoTable({
      startY: y,
      head: [['#', 'HS Code', 'Description of Goods', 'Qty', 'Unit']],
      body: tableData,
      margin: { left: margin, right: margin },
      styles: { fontSize: 9, cellPadding: 3 },
      headStyles: { fillColor: [5, 150, 105], textColor: 255, fontStyle: 'bold' },
      alternateRowStyles: { fillColor: [248, 250, 252] },
    });
    y = doc.lastAutoTable.finalY + 8;
  }

  // Certifying Authority
  doc.setFont(undefined, 'bold');
  doc.text('CERTIFYING AUTHORITY', margin, y);
  y += 5;
  doc.setFont(undefined, 'normal');
  const authority = c.certifying_authority || {};
  const authorityLines = [
    authority.name || 'Korea Chamber of Commerce and Industry (KCCI)',
    authority.address || '',
  ].filter(Boolean);
  authorityLines.forEach(line => { doc.text(line, margin, y); y += 4.5; });
  y += 4;

  // Declaration
  doc.setFont(undefined, 'bold');
  doc.text('DECLARATION', margin, y);
  y += 5;
  doc.setFont(undefined, 'normal');
  const declaration = c.declaration ||
    'The undersigned hereby declares that the above-mentioned goods originate in the country shown above and comply with the origin requirements specified for those goods.';
  const declLines = doc.splitTextToSize(declaration, pageWidth - margin * 2);
  doc.text(declLines, margin, y);
  y += declLines.length * 4.5 + 8;

  // Signature area
  doc.setDrawColor(200);
  doc.line(margin, y, margin + 60, y);
  doc.line(pageWidth - margin - 60, y, pageWidth - margin, y);
  y += 5;
  doc.setFontSize(8);
  doc.text('Exporter Signature', margin, y);
  doc.text('Authorized Officer / Stamp', pageWidth - margin - 60, y);

  // Footer
  y = doc.internal.pageSize.getHeight() - 15;
  doc.setTextColor(150);
  doc.setFontSize(8);
  doc.text('Generated by Whistle AI - Export Management Platform', pageWidth / 2, y, { align: 'center' });

  doc.save(`${document.doc_number || 'CO'}_${dateStr.replace(/\//g, '-')}.pdf`);
}

function fmt(n) { const v = Number(n); return Number.isFinite(v) ? v.toLocaleString() : '0'; }
