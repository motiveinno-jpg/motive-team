// Renders an Export Declaration PDF from document.content using jsPDF + autoTable

export function renderEDToPdf(document) {
  const jsPDF = window.jspdf?.jsPDF;
  if (!jsPDF) { alert('PDF 라이브러리가 로드되지 않았습니다.'); return; }

  const c = document.content || {};
  const doc = new jsPDF('p', 'mm', 'a4');
  const pageWidth = doc.internal.pageSize.getWidth();
  const margin = 15;
  let y = margin;

  // Header
  doc.setFontSize(18);
  doc.setFont(undefined, 'bold');
  doc.setTextColor(71, 85, 105);
  doc.text('EXPORT DECLARATION', pageWidth / 2, y, { align: 'center' });
  y += 7;
  doc.setFontSize(12);
  doc.text('수출신고서', pageWidth / 2, y, { align: 'center' });
  y += 10;

  doc.setTextColor(0);
  doc.setFontSize(10);
  doc.setFont(undefined, 'normal');
  const docNo = document.doc_number || c.declaration_no || 'N/A';
  const dateStr = c.generated_at ? new Date(c.generated_at).toLocaleDateString('en-US') : new Date().toLocaleDateString('en-US');
  doc.text(`Declaration No: ${docNo}`, margin, y);
  doc.text(`Date: ${dateStr}`, pageWidth - margin, y, { align: 'right' });
  y += 5;
  doc.setDrawColor(71, 85, 105);
  doc.setLineWidth(0.5);
  doc.line(margin, y, pageWidth - margin, y);
  y += 8;

  // Exporter info
  doc.setFontSize(9);
  doc.setFont(undefined, 'bold');
  doc.text('EXPORTER', margin, y);
  y += 5;
  doc.setFont(undefined, 'normal');
  const exporter = c.exporter || c.seller || {};
  const exporterLines = [
    exporter.company || exporter.name || 'N/A',
    exporter.address || '',
    exporter.business_no ? `Business No: ${exporter.business_no}` : '',
  ].filter(Boolean);
  exporterLines.forEach(line => { doc.text(line, margin, y); y += 4.5; });
  y += 4;

  // Declaration details in a structured layout
  doc.setFont(undefined, 'bold');
  doc.text('DECLARATION DETAILS', margin, y);
  y += 6;
  doc.setFont(undefined, 'normal');

  const details = [
    ['HS Code', c.hs_code || 'N/A'],
    ['Product Description', c.product_description || c.description || 'N/A'],
    ['Quantity', c.quantity ? fmt(c.quantity) + (c.unit ? ` ${c.unit}` : '') : 'N/A'],
    ['FOB Value', c.fob_value ? cur(c.fob_value, c.currency) : 'N/A'],
    ['Destination Country', c.destination_country || 'N/A'],
    ['Loading Port', c.loading_port || c.port_of_loading || 'N/A'],
    ['Carrier', c.carrier || 'N/A'],
  ];

  doc.autoTable({
    startY: y,
    body: details.map(([label, value]) => [label, value]),
    margin: { left: margin, right: margin },
    styles: { fontSize: 9, cellPadding: 3 },
    columnStyles: {
      0: { fontStyle: 'bold', cellWidth: 45, fillColor: [241, 245, 249] },
      1: { cellWidth: 'auto' },
    },
    theme: 'grid',
    headStyles: { fillColor: [71, 85, 105] },
  });
  y = doc.lastAutoTable.finalY + 8;

  // Items table (if multiple items)
  const items = Array.isArray(c.items) ? c.items : [];
  if (items.length > 0) {
    doc.setFont(undefined, 'bold');
    doc.text('ITEMS', margin, y);
    y += 5;

    const tableData = items.map((item, idx) => [
      idx + 1,
      item.hs_code || c.hs_code || '-',
      item.product_name || item.description || 'N/A',
      fmt(item.qty),
      item.fob_value ? cur(item.fob_value, c.currency) : cur(item.amount, c.currency),
    ]);

    doc.autoTable({
      startY: y,
      head: [['#', 'HS Code', 'Description', 'Qty', 'FOB Value']],
      body: tableData,
      margin: { left: margin, right: margin },
      styles: { fontSize: 9, cellPadding: 3 },
      headStyles: { fillColor: [71, 85, 105], textColor: 255, fontStyle: 'bold' },
      alternateRowStyles: { fillColor: [248, 250, 252] },
    });
    y = doc.lastAutoTable.finalY + 8;
  }

  // Notes
  if (c.notes || document.notes) {
    doc.setFontSize(9);
    doc.setFont(undefined, 'bold');
    doc.text('Remarks:', margin, y);
    y += 5;
    doc.setFont(undefined, 'normal');
    const lines = doc.splitTextToSize(c.notes || document.notes, pageWidth - margin * 2);
    doc.text(lines, margin, y);
    y += lines.length * 4.5 + 4;
  }

  // Signature
  y += 10;
  doc.setDrawColor(200);
  doc.line(margin, y, margin + 60, y);
  y += 5;
  doc.setFontSize(8);
  doc.text('Declarant Signature', margin, y);

  // Footer
  y = doc.internal.pageSize.getHeight() - 15;
  doc.setTextColor(150);
  doc.setFontSize(8);
  doc.text('Generated by Whistle AI - Export Management Platform', pageWidth / 2, y, { align: 'center' });

  doc.save(`${document.doc_number || 'ED'}_${dateStr.replace(/\//g, '-')}.pdf`);
}

function fmt(n) { const v = Number(n); return Number.isFinite(v) ? v.toLocaleString() : '0'; }
function cur(n, c = 'USD') {
  const v = Number(n); if (!Number.isFinite(v)) return '-';
  const s = { USD: '$', KRW: '₩', JPY: '¥', EUR: '€' }[c] || c + ' ';
  return s + v.toLocaleString(undefined, { minimumFractionDigits: 2, maximumFractionDigits: 2 });
}
