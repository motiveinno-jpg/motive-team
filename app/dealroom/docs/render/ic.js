// Renders an Inspection Certificate PDF from document.content using jsPDF + autoTable

export function renderICToPdf(document) {
  const jsPDF = window.jspdf?.jsPDF;
  if (!jsPDF) { alert('PDF 라이브러리가 로드되지 않았습니다.'); return; }

  const c = document.content || {};
  const doc = new jsPDF('p', 'mm', 'a4');
  const pageWidth = doc.internal.pageSize.getWidth();
  const margin = 15;
  let y = margin;

  // Header
  doc.setFontSize(20);
  doc.setFont(undefined, 'bold');
  doc.setTextColor(13, 148, 136);
  doc.text('INSPECTION CERTIFICATE', pageWidth / 2, y, { align: 'center' });
  y += 10;

  doc.setTextColor(0);
  doc.setFontSize(10);
  doc.setFont(undefined, 'normal');
  const docNo = document.doc_number || c.certificate_no || 'N/A';
  const dateStr = c.generated_at ? new Date(c.generated_at).toLocaleDateString('en-US') : new Date().toLocaleDateString('en-US');
  doc.text(`Certificate No: ${docNo}`, margin, y);
  doc.text(`Date: ${dateStr}`, pageWidth - margin, y, { align: 'right' });
  y += 5;
  doc.setDrawColor(13, 148, 136);
  doc.setLineWidth(0.5);
  doc.line(margin, y, pageWidth - margin, y);
  y += 8;

  // Inspector info
  doc.setFontSize(9);
  doc.setFont(undefined, 'bold');
  doc.text('INSPECTOR', margin, y);
  y += 5;
  doc.setFont(undefined, 'normal');
  const inspector = c.inspector || {};
  const inspectorLines = [
    inspector.company || inspector.name || 'N/A',
    inspector.address || '',
    inspector.license ? `License: ${inspector.license}` : '',
  ].filter(Boolean);
  inspectorLines.forEach(line => { doc.text(line, margin, y); y += 4.5; });
  y += 4;

  // Inspection details
  doc.setFont(undefined, 'bold');
  doc.text('INSPECTION DETAILS', margin, y);
  y += 5;
  doc.setFont(undefined, 'normal');
  const inspectionDate = c.inspection_date ? new Date(c.inspection_date).toLocaleDateString('en-US') : dateStr;
  const detailLines = [
    `Inspection Date: ${inspectionDate}`,
    `Inspection Location: ${c.inspection_location || c.location || 'N/A'}`,
    c.reference_no ? `Reference No: ${c.reference_no}` : '',
  ].filter(Boolean);
  detailLines.forEach(line => { doc.text(line, margin, y); y += 4.5; });
  y += 4;

  // Items inspected table
  const items = Array.isArray(c.items) ? c.items : [];
  if (items.length > 0) {
    const tableData = items.map((item, idx) => [
      idx + 1,
      item.product_name || item.description || 'N/A',
      fmt(item.qty),
      item.specification || '-',
      item.result || 'PASSED',
    ]);

    doc.autoTable({
      startY: y,
      head: [['#', 'Description', 'Qty Inspected', 'Specification', 'Result']],
      body: tableData,
      margin: { left: margin, right: margin },
      styles: { fontSize: 9, cellPadding: 3 },
      headStyles: { fillColor: [13, 148, 136], textColor: 255, fontStyle: 'bold' },
      alternateRowStyles: { fillColor: [248, 250, 252] },
      didParseCell: function(data) {
        if (data.section === 'body' && data.column.index === 4) {
          const val = (data.cell.raw || '').toUpperCase();
          if (val === 'PASSED') data.cell.styles.textColor = [5, 150, 105];
          if (val === 'FAILED') data.cell.styles.textColor = [220, 38, 38];
        }
      },
    });
    y = doc.lastAutoTable.finalY + 8;
  }

  // Overall result
  const overallResult = (c.result || 'PASSED').toUpperCase();
  doc.setFont(undefined, 'bold');
  doc.setFontSize(14);
  const isPass = overallResult === 'PASSED';
  doc.setTextColor(isPass ? 5 : 220, isPass ? 150 : 38, isPass ? 105 : 38);
  doc.text(`OVERALL RESULT: ${overallResult}`, pageWidth / 2, y, { align: 'center' });
  doc.setTextColor(0);
  y += 10;

  // Remarks
  if (c.remarks || c.notes || document.notes) {
    doc.setFontSize(9);
    doc.setFont(undefined, 'bold');
    doc.text('Remarks:', margin, y);
    y += 5;
    doc.setFont(undefined, 'normal');
    const lines = doc.splitTextToSize(c.remarks || c.notes || document.notes, pageWidth - margin * 2);
    doc.text(lines, margin, y);
    y += lines.length * 4.5 + 4;
  }

  // Inspector signature
  y += 10;
  doc.setDrawColor(200);
  doc.line(margin, y, margin + 60, y);
  y += 5;
  doc.setFontSize(8);
  doc.text('Inspector Signature', margin, y);
  y += 4;
  doc.text(inspector.name || '', margin, y);

  // Footer
  y = doc.internal.pageSize.getHeight() - 15;
  doc.setTextColor(150);
  doc.setFontSize(8);
  doc.text('Generated by Whistle AI - Export Management Platform', pageWidth / 2, y, { align: 'center' });

  doc.save(`${document.doc_number || 'IC'}_${dateStr.replace(/\//g, '-')}.pdf`);
}

function fmt(n) { const v = Number(n); return Number.isFinite(v) ? v.toLocaleString() : '0'; }
